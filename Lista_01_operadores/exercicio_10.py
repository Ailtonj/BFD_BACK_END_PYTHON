# Crie um arquivo .py, crie um programa que vai conter duas variáveis, valor1 com o valor
# 10.50 e valor2 com o valor 12.00. Use o operador de diferença [ != ] para verificar se elas
# são diferentes e exiba o resultado na tela seguindo essa estrutura "valor1 é igual ao valor2?
# operação_de_comparação".
# Obs: Utilize o Use o operador de diferença [ != ], e print() para mostrar o texto na tela.

valor1 = 10.50
valor2 = 12.00

# Na questão tava mandando colocar "valor1 é igual ao valor2?
# Mas iria ficar sem lógica, pois ele retornaria falso quando fossem iguais e verdadeiro
# quando fossem diferentes. por isso mudei.
print(f"{valor1} é diferente de {valor2}? {valor1 != valor2}")
